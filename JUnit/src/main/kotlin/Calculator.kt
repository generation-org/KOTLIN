class Calculator {
    fun addNumbers(num1: Int, num2: Int): Int {
        return num1 + num2
    }

    fun subtractNumbers(num1: Int, num2: Int): Int {
        return num1 - num2
    }

    fun divideNumbers(num1: Int, num2: Int): Int {
        return num1 / num2
    }

    fun multiplyNumbers(num1: Int, num2: Int): Int {
        return num1 * num2
    }
}